#include<iostream>
using namespace std;
int main(){
    //addition
    cout<<"Sum="<<5+2<<endl;
    //substraction
    cout<<"Difference="<<5-2<<endl;
    //Multiplication
    cout<<"Product="<<5*2<<endl;
    //Division
    cout<<"Quotient="<<5/2<<endl;
    //Modulo
    cout<<"Remainder="<<5%2<<endl;
    //Division in Decimal
    cout<<"quotient="<<5/(double)2<<endl;
    //OR Operator(||)
    cout<<((3>1)||(3>2))<<endl;
    cout<<((3<1)||(3>2))<<endl;
    cout<<((3<1)||(3<2))<<endl;
    //AND Operator(&&)
    cout<<((3>1)&&(3>2))<<endl;
    cout<<((3<1)&&(3>2))<<endl;
    cout<<((3<1)&&(3<2))<<endl;
    //NOT Operator(!)
    cout<<(3!=4)<<endl;
    cout<<(3!=3)<<endl;
    

    return 0;
}